async function assertWrapperResponse (response: Awaited<ReturnType<typeof loadWithMediaViewer>>): Promise<void> {
  expect(response.status()).toBe(200)
  expect(response.headers()['content-type']).toBe('text/html; charset=utf-8')
}

async function readWrapperProps (page: Page): Promise<typeof globalThis.renderMedia> {
  return page.evaluate(() => globalThis.renderMedia)
}

async function assertDownloadButton (page: Page, expectedFilename: string): Promise<void> {
  const button = page.locator('a:has-text("Download")')
  await expect(button).toBeVisible()
  const href = await button.getAttribute('href')
  expect(href).toContain('download=true')
  expect(href).toContain(`filename=${encodeURIComponent(expectedFilename)}`)
}
