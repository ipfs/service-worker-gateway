/**
 * End-to-end coverage for the media-viewer wrapper, the workaround for
 * the Chromium "Save As" bug introduced in #574.
 *
 * Each media type runs through two URL shapes:
 *
 * 1. `/ipfs/<dir>/<filename>`: verified-fetch reads the content-type from
 * the path extension, and `displayName` matches the filename.
 * 2. `/ipfs/<file>` (bare CID): verified-fetch sniffs the bytes,
 * `displayName` falls back to `<cid>`, and `filename` becomes
 * `<cid>.<ext>`.
 *
 * Fixtures live as checked-in CAR files generated by
 * `test-e2e/fixtures/data/generate.ts`; the suite needs no kubo IO at
 * runtime.
 *
 * Each fixture exists in two encodings, dag-pb and raw-codec. The active
 * tests use the dag-pb form because dag-pb files route through
 * `plugin-handle-unixfs`, which sniffs correctly. Raw-codec files
 * short-circuit content-type sniffing in verified-fetch's
 * `plugin-handle-raw` today; the commented-out block at the end of the
 * file pins those CIDs and re-activates as soon as upstream lands a fix.
 */
